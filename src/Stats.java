public class Stats {
    private int Strength = 10;
    private int MaxIntelligence = 100;
    private int Intelligence = 100;
    private int MaxStamina = 100;
    private int Stamina = 100;
    private int Hp = 100;
    private int Defense = 10;
    private int CriticalChance = 10;
    private int CriticalPower = 20;

    public int getStrength() {
        return Strength;
    }

    public int getMaxIntelligence() {
        return MaxIntelligence;
    }

    public int getIntelligence() {
        return Intelligence;
    }

    public int getMaxStamina() {
        return MaxStamina;
    }

    public int getStamina() {
        return Stamina;
    }

    public int getHp() {
        return Hp;
    }

    public int getDefense() {
        return Defense;
    }

    public int getCriticalChance() {
        return CriticalChance;
    }

    public int getCriticalPower() {
        return CriticalPower;
    }

    public void setStrength(int strength) {
        Strength = strength;
    }

    public void setMaxIntelligence(int maxIntelligence) {
        MaxIntelligence = maxIntelligence;
    }

    public void setIntelligence(int intelligence) {
        Intelligence = intelligence;
    }

    public void setMaxStamina(int maxStamina) {
        MaxStamina = maxStamina;
    }

    public void setStamina(int stamina) {
        Stamina = stamina;
    }

    public void setHp(int hp) {
        Hp = hp;
    }

    public void setDefense(int defense) {
        Defense = defense;
    }

    public void setCriticalChance(int criticalChance) {
        CriticalChance = criticalChance;
    }

    public void setCriticalPower(int criticalPower) {
        CriticalPower = criticalPower;
    }
}
